void portWrite(int block,int pinval)
{
	if(block<10)
	{
	if(block==0)
	{
		IODIR0|=0Xff;
		if(pinval==0)
		{
			IOCLR0|=0Xff;
		}
		else
		{
			IOSET0|=0Xff;
		}
	}
	if(block==1)
	{
		IODIR0|=0Xff00;
		if(pinval==0)
		{
			IOCLR0|=0Xff00;
		}
		else
		{
			IOSET0|=0Xff00;
		}
	}
	if(block==2)
	{
		IODIR0|=0Xff0000;
		if(pinval==0)
		{
			IOCLR0|=0Xff0000;
		}
		else
		{
			IOSET0|=0Xff0000;
		}
	}
	if(block==3)
	{
		IODIR0|=0Xff000000;
		if(pinval==0)
		{
			IOCLR0|=0Xff000000;
		}
		else
		{
			IOSET0|=0Xff000000;
		}
	}
	}
	else
	{
	if(block==12)
	{
		IODIR1|=0X00ff0000;
		if(pinval==0)
		{
			IOCLR1|=0X00ff0000;
		}
		else
		{
			IOSET1|=0Xff0000;
		}
	}
	else if(block==13)
	{
		IODIR1|=0X00ff000000;
		if(pinval==0)
		{
			IOCLR1|=0Xff000000;
		}
		else
		{
			IOSET1|=0Xff000000;
		}
	}
}
}
